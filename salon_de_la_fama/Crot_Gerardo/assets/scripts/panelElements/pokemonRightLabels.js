export function createTotalLabel(data, x, y, width, svg, color){
    svg.append('rect')
        .attr('rx', 20)
        .attr('ry', 20)
        .attr('x', x)
        .attr('y', y)
        .attr('height', 40)
        .attr('width', width)
        .attr('fill', color)
        .attr('stroke', ' #141414')
        .attr('stroke-width', 1)

    svg.append('rect')
        .attr('rx', 20)
        .attr('ry', 20)
        .attr('x', x+3*width/5)
        .attr('y', y)
        .attr('height', 40)
        .attr('width', 2*width/5)
        .attr('fill', 'white')

    svg.append('rect')
        .attr('x', x+3*width/5)
        .attr('y', y)
        .attr('height', 40)
        .attr('width', 20)
        .attr('fill', 'white')

    svg.append('text')
        .attr('text-anchor', 'end')
        .attr('x', x + 3*width/5 - 5)
        .attr('y', y + 25)
        .text("Total Base Points:")
        .attr('font-size', 15)
        .attr('fill', 'white')

    svg.append('text')
        .attr('text-anchor', 'start')
        .attr('x', x + 3*width/5 + 5)
        .attr('y', y + 25)
        .text(data["total_points"])
        .attr('font-size', 15)
        .attr('fill', ' #141414')
}

export function createTypeLabel(data, x, y, width, svg, color, typeColors){
    svg.append('rect')
        .attr('rx', 20)
        .attr('ry', 20)
        .attr('x', x)
        .attr('y', y)
        .attr('height', 40)
        .attr('width', width)
        .attr('fill', color)
        .attr('stroke', ' #141414')
        .attr('stroke-width', 1)

    svg.append('rect')
        .attr('rx', 20)
        .attr('ry', 20)
        .attr('x', x+1.5*width/5)
        .attr('y', y)
        .attr('height', 40)
        .attr('width', 3.5*width/5)
        .attr('fill', 'white')

    svg.append('rect')
        .attr('x', x+1.5*width/5)
        .attr('y', y)
        .attr('height', 40)
        .attr('width', 20)
        .attr('fill', 'white')

    svg.append('text')
        .attr('text-anchor', 'middle')
        .attr('x', x + (1.5*width/5)/2)
        .attr('y', y + 25)
        .text("Types:")
        .attr('width', 1.5*width/5 - 10)
        .attr('fill', 'white')

    svg.append('rect')
        .attr('rx', 15)
        .attr('ry', 15)
        .attr('x', x + 1.5*width/5 + 5)
        .attr('y', y + 5)
        .attr('height', 30)
        .attr('width', 1.5*width/5)
        .attr('fill', typeColors[data["type_1"]])
        .attr('stroke', ' #141414')
        .attr('stroke-width', 1)

    svg.append('text')
        .attr('text-anchor', 'middle')
        .attr('x', x + 1.5*width/5 + 5 + 1.5* width/10)
        .attr('y', y + 25)
        .text(data["type_1"])
        .attr('width', 1.3* width/5 - 5)
        .attr('fill', 'white')

    if (data["type_number"]>1){
        svg.append('rect')
            .attr('rx', 15)
            .attr('ry', 15)
            .attr('x', x+2.75*width/5 + 25)
            .attr('y', y + 5)
            .attr('height', 30)
            .attr('width', 1.5* width/5)
            .attr('fill', typeColors[data["type_2"]])
            .attr('stroke', ' #141414')
            .attr('stroke-width', 1)

        svg.append('text')
            .attr('text-anchor', 'middle')
            .attr('x', x+2.75*width/5 + 25 + 1.5* width/10)
            .attr('y', y + 25)
            .text(data["type_2"])
            .attr('fill', 'white')
            .attr('width', 1.5* width/5)
    }
}  