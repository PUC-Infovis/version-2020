export function createStatusLabel(data, x, y, width, svg){
    let color = {
        "Normal": '#A8A878',
        "Sub Legendary": '#BEC2CB',
        "Legendary": '#D4AF37',
        "Mythical": '#9966CC'
    }

    svg.append('rect')
        .attr('rx', 20)
        .attr('ry', 20)
        .attr('x', x)
        .attr('y', y + 10)
        .attr('height', 40)
        .attr('width', width)
        .attr('fill', color[data["status"]])
        .attr('stroke', ' #141414')
        .attr('stroke-width', 1)

    svg.append('text')
        .attr('text-anchor', 'middle')
        .attr('x', x + width/2)
        .attr('y', y + 35)
        .text(data["status"])
        .attr('width', width)
        .attr('fill', 'white')
}

